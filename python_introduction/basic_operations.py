#!/usr/bin/python3

number1 = 10
number2 = 5

"""Addition of numbers"""
sum = number1 + number2

"""Subtraction"""
diff = number1 - number2

"""Multiplication"""
mul = number1 * number2

print(f"Addition of {number1} and {number2} is {sum}")
print(f"Subtraction of {number1} and {number2} is {diff}")
print(f"Multiplication of {number1} and {number2} is {mul}")
